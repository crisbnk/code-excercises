// Check if a value is classified as a boolean primitive. Return true or false.
// Boolean primitives are true and false.

function boo(bool) {
  var x = typeof bool,
      res = null;
  if (x === "boolean") {
    return res  = true;
  } else {
    return res = false;
  }
  
  return res;
}
